    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        // TODO add your handling code here:
        try {
            if (btnNuevo.getText().equals("Nuevo")) {
                LimpiarDatos();
                ActivarControles(true);
                txtNombre.requestFocus();
                Nuevo = true;
                Editar = false;
                btnNuevo.setText("Guardar");
                btnEditar.setText("Cancelar");
            } else {  //Guardar - Nuevo o Modificar
                String confirmMessage;
                if (Nuevo) {
                    confirmMessage = "¿Estás seguro de agregar el empleado?";
                } else {
                    confirmMessage = "¿Estás seguro de editar el registro?";
                }

                Object[] options = {"SI", "NO"};
                int option = JOptionPane.showOptionDialog(null, confirmMessage, "Confirmación", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, options, options[0]);
                if (option != JOptionPane.YES_OPTION) {
                    return; // Si el usuario elige "NO", se sale del método sin realizar ninguna acción.
                }

                Nombre = txtNombre.getText();
                UserName = txtUserName.getText();
                Clave = txtClave.getText();
                Email = txtEmail.getText();
                IdRol = Integer.parseInt(cbRol.getSelectedItem().toString().split("-")[0]);
                Estado = chkEstado.isSelected();
                Confirmado = chkConfirmado.isSelected();
                Restablecer = chkRestablecer.isSelected();
                if (Nuevo) {
                    int resp = cSql.AgregarEmpleado(Nombre, UserName, Clave, Email, IdRol, Estado, Confirmado, Restablecer);
                    if (resp > 0) {
                        JOptionPane.showMessageDialog(null, "El empleado se creó de forma exitosa! Id=" + resp);
                    } else {
                        JOptionPane.showMessageDialog(null, "El empleado no se pudo crear!");
                    }
                } else {//Modificaciones
                     IdEmpleado = Integer.parseInt(txtIdEmpleado.getText());
    int resp = cSql.ModificarEmpleado(IdEmpleado, Nombre, UserName, Clave, Email, IdRol, Estado, Confirmado, Restablecer);
    if (resp > 0) {
        JOptionPane.showMessageDialog(null, "El registro se modificó de forma exitosa!");
        cargarEmpleados(); // Actualizar la tabla después de la modificación exitosa
    } else {
        JOptionPane.showMessageDialog(null, "El registro no se pudo modificar!");
    }
                }
                btnNuevo.setText("Nuevo");
                btnEditar.setText("Editar");
                ActivarControles(false);
                Nuevo = false;
                Editar = false;
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnNuevoActionPerformed

