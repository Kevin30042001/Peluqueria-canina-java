private String obtenerNombreRol(int idRol) {
    String nombreRol = "";
    try {
        ResultSet rolRS = cSql.Rol();
        while (rolRS.next()) {
            if (rolRS.getInt("IdRol") == idRol) {
                nombreRol = rolRS.getString("NombreRol");
                break;
            }
        }
    } catch (SQLException ex) {
        ex.printStackTrace();
    }
    return nombreRol;
}
