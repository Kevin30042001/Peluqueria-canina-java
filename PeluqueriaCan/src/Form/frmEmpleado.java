     private void cargarEmpleados() {
        DefaultTableModel modelotabla = (DefaultTableModel) tbEmpleados.getModel();
        modelotabla.setRowCount(0);

        try {
            dbObjetos db = new dbObjetos();
            ResultSet rs = db.Empleados(); // Llama al método que consulta los empleados

            // Se itera sobre los resultados y se agregan al modelo de la tabla
            while (rs.next()) {
                Object[] fila = new Object[modelotabla.getColumnCount()];

                for (int indice = 0; indice < fila.length; indice++) {
                    fila[indice] = rs.getObject(indice + 1);
                }
                modelotabla.addRow(fila);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
        
         tbEmpleados.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
    @Override
    
    public void valueChanged(ListSelectionEvent e) {
        if (!e.getValueIsAdjusting()) {
            int selectedRow = tbEmpleados.getSelectedRow();
            if (selectedRow >= 0) {
                txtIdEmpleado.setText(tbEmpleados.getValueAt(selectedRow, 0).toString());
                txtNombre.setText(tbEmpleados.getValueAt(selectedRow, 1).toString());
                txtEmail.setText(tbEmpleados.getValueAt(selectedRow, 4).toString());
                txtUserName.setText(tbEmpleados.getValueAt(selectedRow, 2).toString());
                txtClave.setText(tbEmpleados.getValueAt(selectedRow, 3).toString());
                int idRol = (int) tbEmpleados.getValueAt(selectedRow, 5);
                cbRol.setSelectedItem(idRol + "-" + obtenerNombreRol(idRol));
                chkEstado.setSelected((boolean) tbEmpleados.getValueAt(selectedRow, 6));
                chkConfirmado.setSelected((boolean) tbEmpleados.getValueAt(selectedRow, 7));
                chkRestablecer.setSelected((boolean) tbEmpleados.getValueAt(selectedRow, 8));
                txtFecha.setText(tbEmpleados.getValueAt(selectedRow, 10).toString());
           
                
                ActivarControles(false); // Deshabilitar campos de texto
            }
        }
    }
});
    }
    
    
