    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        try {
            if (btnEditar.getText().equals("Editar")) {
                ActivarControles(true);
                txtNombre.requestFocus();
                Nuevo = false;
                Editar = true;
                btnNuevo.setText("Guardar");
                btnEditar.setText("Cancelar");
            } else {  //Guardar - Nuevo o Modificar
                btnNuevo.setText("Nuevo");
                btnEditar.setText("Editar");
                ActivarControles(false);
                Nuevo = false;
                Editar = false;
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        try {
            // Verificar si se ha seleccionado un cliente para eliminar
            if (!txtIdEmpleado.getText().isEmpty()) {
                // Mostrar mensaje de confirmación
                Object[] options = {"Si", "No"};
                int option = JOptionPane.showOptionDialog(null, "¿Estás seguro de eliminar el registro?", "Confirmación", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, options, options[0]);
                if (option == JOptionPane.YES_OPTION) {
                    IdEmpleado = Integer.parseInt(txtIdEmpleado.getText());
                    int resp = cSql.EliminarEmpleado(IdEmpleado);
                    if (resp > 0) {
                        JOptionPane.showMessageDialog(null, "El registro se eliminó de forma exitosa!");
                    } else {
                        JOptionPane.showMessageDialog(null, "El registro no se pudo eliminar!");
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "No se ha seleccionado ningún cliente para eliminar.");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        try {
            //Ejecutar proceso de búsqueda
            if (txtIdEmpleado.isEditable()) {
                rs = cSql.BuscarEmpleado(Integer.parseInt(txtIdEmpleado.getText()), txtNombre.getText(), txtUserName.getText(), txtEmail.getText(), 0, "");
                if (rs.next()) {
                    txtIdEmpleado.setText(rs.getString("IdEmpleado"));
                    txtNombre.setText(rs.getString("Nombre"));
                    txtUserName.setText(rs.getString("UserName"));
                    txtClave.setText(rs.getString("Clave"));
                    txtEmail.setText(rs.getString("Email"));
                    if (rs.getBoolean("Estado")) {
                        chkEstado.setSelected(true);
                    } else {
                        chkEstado.setSelected(false);
                    }
                    if (rs.getBoolean("Confirmado")) {
                        chkConfirmado.setSelected(true);
                    } else {
                        chkConfirmado.setSelected(false);
                    }
                    if (rs.getBoolean("Restablecer")) {
                        chkRestablecer.setSelected(true);
                    } else {
                        chkRestablecer.setSelected(false);
                    }
                    txtFecha.setText(rs.getString("Token"));
                    txtFecha.setText(rs.getString("Fecha"));
                    vRol = String.valueOf(rs.getInt("IdRol")) + "-" + rs.getString("NombreRol");
                    cbRol.setSelectedItem(vRol);
                }
                ActivarControles(false);
                btnNuevo.setEnabled(true);
                btnEditar.setEnabled(true);
            } else {
                ActivarControles(true);
                btnNuevo.setEnabled(false);
                btnEditar.setEnabled(false);
                btnBuscar.setEnabled(true);
                btnSalir.setEnabled(true);
                txtIdEmpleado.requestFocus();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed


